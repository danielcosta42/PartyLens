-- Obtém string localizada
function Localization.Get(key)
    local locale = Localization.CurrentLocale
    
    -- Tenta obter do locale atual
    if Localization.Strings[locale] and Localization.Strings[locale][key] then
        return Localization.Strings[locale][key]
    end
    
    -- Fallback para inglês
    if Localization.Strings.enUS and Localization.Strings.enUS[key] then
        return Localization.Strings.enUS[key]
    end
    
    -- Fallback final
    return key
end

-- Atalho L() para usar em todo o código
function Localization.L(key, ...)
    local str = Localization.Get(key)
    if ... then
        return string.format(str, ...)
    end
    return str
end

-- Alternativa global para facilitar uso
_G.L = function(key, ...)
    return Localization.L(key, ...)
end

_G[ADDON_NAME .. "_Localization"] = Localization
return Localization
